func abs(data []float64) []float64 {
	data = append([]float64(nil), data...)
	for i := range data {
		if data[i] < 0.0 {
			data[i] *= -1.0
		}
	}
	return data
}

func slice(data []float64, m, n, p uint) []float64 {
	result := make([]float64, p*n)
	for i := uint(0); i < p; i++ {
		for j := uint(0); j < n; j++ {
			result[j*p+i] = data[j*m+i]
		}
	}
	return result
}
